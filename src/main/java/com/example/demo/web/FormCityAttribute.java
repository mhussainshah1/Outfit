package com.example.demo.web;

public class FormCityAttribute {

    private String city;

    public FormCityAttribute() {
        super();
    }

    public FormCityAttribute(String city) {
        super();
        this.city = city;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
